#include "utils.h"

u8 bin2bcd(s8 bin)
{
 return (u8)(bin + 6 * (bin / 10));
}

s8 bcd2bin(u8 bcd)
{
 return (s8)(bcd - 6 * (bcd >> 4));
}

u16 freeSram()
{
 extern int __heap_start, *__brkval;
 u16 v;
 return (u16) &v - (__brkval == 0 ? (int) &__heap_start : (int) __brkval);
}
